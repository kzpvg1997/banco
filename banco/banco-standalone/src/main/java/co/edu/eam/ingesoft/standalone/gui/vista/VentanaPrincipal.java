	private void jBbcompraTarjetaConsumoActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jBbcompraTarjetaConsumoActionPerformed

		try {
			if (jTFvalorCompraTarjetaConsumo.getText().isEmpty()) {
				JOptionPane.showMessageDialog(null, "Por favor, ingrese el valor de compra del consumo", "INGRESE",
						JOptionPane.WARNING_MESSAGE);
			} else {

				if (Integer.parseInt(jSPNumeroCuotasConsumo.getValue().toString()) <= 0) {
					JOptionPane.showMessageDialog(null, "Por favor, ingrese el numero de cuotas a pagar", "INGRESE",
							JOptionPane.WARNING_MESSAGE);
					if (Integer.parseInt(jSPNumeroCuotasConsumo.getValue().toString()) > 24) {
						JOptionPane.showMessageDialog(null, "El numero maximo de cuotas permitidas es de 24", "ERROR",
								JOptionPane.WARNING_MESSAGE);
						return;
					}
					return;
				}

				jTFvalorRestanteTarjetaConsumo.setText("0.0");
				int cuotas = Integer.parseInt(jSPNumeroCuotasConsumo.getValue().toString());
				double valorCompra = Double.parseDouble(jTFvalorCompraTarjetaConsumo.getText());
				// double valorRestante =
				// Double.parseDouble(jTFvalorRestanteTarjetaConsumo.getText());
				// El valor restante se opera internamente
				// double interes =
				// Double.parseDouble(jTFinteresTarjetaConsumo.getText()); El
				// interes es el valor * 0.036 (Se opera internamente)
				boolean pagado = false;
				CreditCard cre = (CreditCard) jCBnumeroTarjetaConsumo.getSelectedItem();

				CreditCard tarjeta = conTarjeta.buscarCreditCard(cre.getNumber());

				CreditCardConsume consumo = new CreditCardConsume();
				consumo.setNumberShares(cuotas);
				consumo.setAmmount(valorCompra);
				consumo.setPayed(pagado);
				consumo.setCreditCard(tarjeta);

				conTarjConsumo.crearConsumo(consumo);
				limpiarCamposconsumo();
				cargarConsumosTarjeta(tarjeta);
				JOptionPane.showMessageDialog(null, "Se ha generado un consumo exitosamente", "EXITO",
						JOptionPane.INFORMATION_MESSAGE);

			}

		} catch (ExcepcionNegocio en) {
			JOptionPane.showMessageDialog(null, en.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
		} catch (NumberFormatException ex) {
			JOptionPane.showMessageDialog(null,
					"Verifique que el numero de cuotas y valor de compra solo contengan datos (Numericos)", "ERROR",
					JOptionPane.ERROR_MESSAGE);
		} catch (Exception e) {
			e.printStackTrace();
		}

	}// GEN-LAST:event_jBbcompraTarjetaConsumoActionPerformed

